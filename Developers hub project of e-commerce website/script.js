window.addEventListener("DOMContentLoaded", () => {
  // Restore last search query
  const si = document.getElementById("searchInput");
  const savedQuery = localStorage.getItem("lastSearchQuery");
  if (si && savedQuery) si.value = savedQuery;

  // Restore language
  const savedLang = localStorage.getItem("selectedLang");
  if (savedLang && translations[savedLang]) {
    applyLanguage(savedLang);
    document.querySelectorAll("#lngMenu .nav-dropdown-item").forEach(item => {
      item.classList.toggle("selected", item.textContent.trim() === savedLang);
    });
    const lngTrigger = document.getElementById("lngTrigger");
    if (lngTrigger) lngTrigger.childNodes[0].textContent = savedLang + " ";
  }

  // Restore ship to
  const savedShip = localStorage.getItem("selectedShip");
  if (savedShip) {
    const data = shipData[savedShip];
    if (data) updateShipTrigger(savedShip, data.flag);
    document.querySelectorAll("#shipMenu .nav-dropdown-item").forEach(item => {
      item.classList.toggle("selected", item.dataset.country === savedShip);
    });
  }

  renderRecentSearches();
  initTrendingSearchClicks();

  // Update header profile if logged in
  const user = JSON.parse(localStorage.getItem("ec_currentUser") || "null");
  if (user) {
    const headerProfileSpan = document.querySelector(".header-item span");
    // Find the profile span specifically
    document.querySelectorAll(".header-item").forEach(item => {
      const span = item.querySelector("span");
      const icon = item.querySelector("i");
      if (icon && icon.classList.contains("fa-user") && span) {
        span.textContent = user.name ? user.name[0].toUpperCase() : "U";
      }
    });
  }
});

