// RECENT SEARCHES (localStorage)
// ======================================================

const MAX_RECENT = 8;

function getRecentSearches() {
  try {
    return JSON.parse(localStorage.getItem("recentSearches") || "[]");
  } catch { return []; }
}

function saveRecentSearch(query) {
  if (!query.trim()) return;
  let searches = getRecentSearches().filter(s => s.toLowerCase() !== query.toLowerCase());
  searches.unshift(query.trim());
  if (searches.length > MAX_RECENT) searches = searches.slice(0, MAX_RECENT);
  localStorage.setItem("recentSearches", JSON.stringify(searches));
}

function removeRecentSearch(query) {
  let searches = getRecentSearches().filter(s => s !== query);
  localStorage.setItem("recentSearches", JSON.stringify(searches));
  renderRecentSearches();
}

function clearAllRecent() {
  localStorage.removeItem("recentSearches");
  renderRecentSearches();
}

function renderRecentSearches() {
  const container = document.querySelector(".recent-searches-list");
  if (!container) return;
  const searches = getRecentSearches();
  if (searches.length === 0) {
    container.innerHTML = `<div class="search-item" style="color:#8B96A5;font-style:italic;font-size:13px;">No recent searches</div>`;
    return;
  }
  container.innerHTML = searches.map(s => `
    <div class="search-item recent-item" data-query="${encodeURIComponent(s)}" style="justify-content:space-between;">
      <span style="display:flex;align-items:center;gap:10px;">
        <i class="fa-regular fa-clock"></i>
        <span class="recent-text">${escapeHtml(s)}</span>
      </span>
      <button class="remove-recent-btn" title="Remove"
        onclick="removeRecentSearch('${escapeHtml(s)}');event.stopPropagation();"
        style="background:none;border:none;color:#BCC1C8;cursor:pointer;font-size:13px;padding:2px 4px;">
        <i class="fa-solid fa-xmark"></i>
      </button>
    </div>
  `).join("");

  container.querySelectorAll(".recent-item").forEach(item => {
    item.addEventListener("click", function(e) {
      if (e.target.closest(".remove-recent-btn")) return;
      const q = decodeURIComponent(this.dataset.query);
      const si = document.getElementById("searchInput");
      if (si) { si.value = q; si.focus(); }
    });
  });
}

function escapeHtml(str) {
  return str.replace(/&/g,"&amp;").replace(/</g,"&lt;").replace(/>/g,"&gt;").replace(/"/g,"&quot;").replace(/'/g,"&#39;");
}

// ======================================================
// TRENDING SEARCH → RECENT SEARCH (on click)
// ======================================================

function initTrendingSearchClicks() {
  document.querySelectorAll(".search-item").forEach(item => {
    if (item.querySelector(".fa-arrow-trend-up")) {
      item.style.cursor = "pointer";
      item.addEventListener("click", function() {
        const icon = this.querySelector("i");
        // Get text without the icon text
        let text = "";
        this.childNodes.forEach(node => {
          if (node.nodeType === 3) text += node.textContent.trim();
        });
        text = text.trim();
        if (!text) text = this.textContent.trim();

        saveRecentSearch(text);
        const si = document.getElementById("searchInput");
        if (si) si.value = text;
        localStorage.setItem("lastSearchQuery", text);
        renderRecentSearches();
      });
    }
  });
}

