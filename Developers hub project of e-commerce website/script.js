function updateShipTrigger(countryName, flagSrc) {
  if (!shipTrigger) return;
  const chevron = shipTrigger.querySelector(".chevron");
  shipTrigger.innerHTML = "";
  shipTrigger.appendChild(document.createTextNode(countryName + " "));
  const img = document.createElement("img");
  img.src = flagSrc;
  img.alt = countryName + " flag";
  shipTrigger.appendChild(img);
  if (chevron) shipTrigger.appendChild(chevron);
  else {
    const ic = document.createElement("i");
    ic.className = "fa-solid fa-chevron-down chevron";
    shipTrigger.appendChild(ic);
  }
}

function selectShip(el, countryKey) {
  document.querySelectorAll("#shipMenu .nav-dropdown-item").forEach(item => item.classList.remove("selected"));
  el.classList.add("selected");
  const data = shipData[countryKey];
  if (data) {
    updateShipTrigger(countryKey, data.flag);
    localStorage.setItem("selectedShip", countryKey);
  }
  shipDropdown.classList.remove("active");
}

